package com.company;

public class Main {

    public static void main(String[] args) {

	    // Обявляєм дві перемінні
        int var1;
        int var2;
        int var3;

        // Присвоюємо їм значення
        var1 = 4096;
        var2 = var1 / 4;
        var3 = var2 * 8;

        // виводимо значення перемінних
        System.out.println("var1 = " + var1);
        System.out.println("var2 = var1 / 4 = " + var2);
        System.out.println("var3 = var2 * 8 = " + var3);
    } //main()
} // class main
